import UIKit

extension UIColor {
	class var primaryDarkGreen: UIColor {
		return UIColor(red: 73.0 / 255.0, green: 228.0 / 255.0, blue: 185.0 / 255.0, alpha: 1.0)
	}
	class var primaryLightGreen: UIColor {
		return UIColor(red: 85.0 / 255.0, green: 239.0 / 255.0, blue: 196.0 / 255.0, alpha: 1.0)
	}
	class var darkGray: UIColor {
		return UIColor(white: 56.0 / 255.0, alpha: 1.0)
	}
	class var mediumGray: UIColor {
		return UIColor(white: 130.0 / 255.0, alpha: 1.0)
	}
	class var detailBlue: UIColor {
		return UIColor(red: 9.0 / 255.0, green: 132.0 / 255.0, blue: 227.0 / 255.0, alpha: 1.0)
	}
	class var lightGray: UIColor {
		return UIColor(white: 242.0 / 255.0, alpha: 1.0)
	}
	class var detailYellow: UIColor {
		return UIColor(red: 1.0, green: 234.0 / 255.0, blue: 167.0 / 255.0, alpha: 1.0)
	}
}
